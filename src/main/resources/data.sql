DROP TABLE IF EXISTS PRICES;
DROP TABLE IF EXISTS BRAND;

CREATE TABLE BRAND (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(255) NOT NULL
);

CREATE TABLE PRICES (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    BRAND_ID INT NOT NULL,
    START_DATE DATETIME NOT NULL,
    END_DATE DATETIME NOT NULL,
    PRICE_LIST INT NOT NULL,
    PRODUCT_ID INT NOT NULL,
    PRIORITY INT NOT NULL,
    PRICE DECIMAL(10, 2) NOT NULL,
    CURR VARCHAR(3) NOT NULL,
    CONSTRAINT FK_PRICES_BRAND FOREIGN KEY (BRAND_ID) REFERENCES BRAND(ID)
);

-- Index for the foreign key
CREATE INDEX IDX_PRICES_BRAND_ID ON PRICES (BRAND_ID);

-- Composite index to optimize the main query for finding prices
CREATE INDEX IDX_PRICES_COMPOUND ON PRICES (BRAND_ID, PRODUCT_ID, START_DATE, END_DATE, PRIORITY);

